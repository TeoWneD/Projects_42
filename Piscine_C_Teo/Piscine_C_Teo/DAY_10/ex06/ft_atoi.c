#include "do_op.h"

int	ft_atoi(const char *str)
{
	int	j;
	int	i;

	j = 0;
	if (str == NULL || str == 0 || str[0] == '\0')
		return (j);
	i = 1;
	if (*str == '-' || *str == '+')
	{
		i = (*str == '-') ? -i : i;
		str++;
	}
	while (*str && *str >= 48 && *str <= 57)
	{
		j = j * 10 + (*str - '0');
		str++;
	}
	return (j * i);
}
