int             ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int     i;
	int     j;

	i = 0;
	j = 0;
	while (i < length - 1 && j <= 0)
	{
		j = f(tab[i], tab[i + 1]);
		i++;
	}
	j = (j <= 0) ? 1 : 0;
	return (j);
}
