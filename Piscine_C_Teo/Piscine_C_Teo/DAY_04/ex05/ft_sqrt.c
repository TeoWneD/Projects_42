int		ft_sqrt(int nb)
{
	int a;
	int res;

	a = 1;
	if (nb < 0)
		return (0);
	while (nb >= a * a)
	{
		res = nb / a;
		if (a == res && (nb % a) == 0)
		{
			return (a);
		}
		a++;
	}
	return (0);
}
